package com.quickitdotnet.java;

public class Example002_DataTypes2 {

	public static void main(String[] args) {

		// how to define variable with Data Type 
		
		// variable => Container which holds value 
		
		int a = 20;
		System.out.println("The value of variable a is: "+ a);
		
		long b = 2087543443;
		System.out.println("The value of variable b is: "+ b);
		
		float c = 20.789f;
		System.out.println("The value of variable c is: "+ c);
		
		
		double d = 2008746454544d;
		System.out.println("The value of variable d is: "+ d);
		
		char name = 'G';
		System.out.println("The value of Character name is: "+ name);
		
		char name1 = 24;
		System.out.println("The value of Character name is: "+ name1);
	}

}
